const fs = require('fs');
const solc = require('solc');

const input = JSON.parse(fs.readFileSync('DailyGM-MultiFile-Json.json', 'utf8'));

const output = JSON.parse(solc.compile(JSON.stringify(input)));

if (output.errors && output.errors.filter(e => e.severity === 'error').length > 0) {
    console.error("Compilation errors:", output.errors);
    process.exit(1);
}

const bytecode = output.contracts['src/DailyGM.sol'].DailyGM.evm.bytecode.object;

console.log("Generated Bytecode Prefix:");
console.log(bytecode.substring(0, 150));

if (bytecode.includes('5f5ffd')) {
    console.log("\nSuccess! We hit the exact PUSH0 PUSH0 REVERT sequence that matches the blockchain!");
} else if (bytecode.includes('5f80fd')) {
    console.log("\nFailed! Still generated PUSH0 DUP1 REVERT (5f80fd)");
} else {
    console.log("\nGenerated something else!");
}
